# variant9_is_sorted.py
# Вариант 9 — Рекурсивная проверка, отсортирован ли массив (неубывающий порядок)
#
# Комментарии:
# - Функция проверяет arr[i] <= arr[i+1] и рекурсивно вызывает проверку для сдвинутого индекса.
# - Работает для произвольного последовательного индексируемого типа, поддерживающего сравнение.

from typing import List, Any

def is_sorted_recursive(arr: List[Any], index: int = 0) -> bool:
    """
    Возвращает True если arr упорядочен по неубыванию, иначе False.
    index — текущая позиция для сравнения arr[index] и arr[index+1].
    """
    # Если длина 0 или 1 или мы дошли до предпоследнего элемента — массив отсортирован
    if index >= len(arr) - 1:
        return True  # Базовый случай: массив пустой, из одного элемента или конец проверки
    # Если текущая пара нарушает порядок — сразу False
    if arr[index] > arr[index + 1]:
        return False
    # Иначе проверяем дальше
    return is_sorted_recursive(arr, index + 1)  # Рекурсивный вызов для следующего индекса

if __name__ == "__main__":
    tests = [
        [],
        [1],
        [1, 2, 2, 3],
        [3, 2, 1],
        [1, 3, 2],
        ["a", "b", "c"],
    ]
    for t in tests:
        print(f"{t} -> is_sorted: {is_sorted_recursive(t)}")
