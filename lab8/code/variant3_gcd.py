# variant3_gcd.py
# Вариант 3 — Рекурсивный НОД (алгоритм Евклида)
#
# Комментарии:
# - Простая и эффективная рекурсивная реализация.
# - Обрабатывает положительные целые числа.
# - Возвращает неотрицательный НОД; gcd(0, 0) по определению считается 0 здесь.

from typing import Tuple

def gcd(a: int, b: int) -> int:
    """
    Рекурсивный алгоритм Евклида для НОД.
    gcd(a, b) = gcd(b, a % b), пока b != 0.
    Базовый случай: если b == 0 -> возвращаем abs(a) (чтобы поддержать отрицательные входы).
    """
    # Нормализация: работаем с абсолютными значениями
    a, b = abs(a), abs(b)
    if b == 0:
        return a  # Если b равно 0, возвращаем a как НОД
    return gcd(b, a % b)  # Рекурсивный вызов с обновленными параметрами

if __name__ == "__main__":
    # Простые тесты — демонстрация работы
    examples: Tuple[Tuple[int,int], ...] = (
        (48, 18),   # gcd = 6
        (0, 5),     # gcd = 5
        (5, 0),     # gcd = 5
        (0, 0),     # определение: 0
        (270, 192), # gcd = 6
        (-24, 18),  # поддержка отрицательных входов -> 6
    )

    for a, b in examples:
        print(f"gcd({a}, {b}) = {gcd(a, b)}")
