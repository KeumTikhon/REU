"""Сортировка пузырьком (Bubble Sort).

Простой итеративный алгоритм: многократно проходим по массиву,
сравниваем соседние элементы и меняем их местами, если они находятся
в неправильном порядке. Повторяем, пока массив не будет отсортирован.
"""


def bubble_sort(arr):
    n = len(arr)
    # Проходим по всем элементам массива
    for i in range(n - 1):
        # Последний элемент на каждой итерации уже на своем месте
        for j in range(n - i - 1):
            # Сравниваем соседние элементы
            if arr[j] > arr[j + 1]:
                # Меняем местами, если они стоят в неправильном порядке
                arr[j], arr[j + 1] = arr[j + 1], arr[j]


def print_array(arr):
    print(" ".join(map(str, arr)))


if __name__ == "__main__":
    arr = [45, 12, 78, 3, 56, 29, 8]
    print("Исходный массив:")
    print_array(arr)

    bubble_sort(arr)

    print("Отсортированный массив:")
    print_array(arr)